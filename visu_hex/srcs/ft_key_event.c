#include <visu_hex.h>

int			ft_key_event(int keycode, t_b *b)
{
	if (keycode == 49)
	{
		ft_next_step(b);
		ft_print_result(b);
	}
	else if (keycode == 53)
	{
		ft_close(b);
		exit(EXIT_SUCCESS);
	}
	return (0);
}
