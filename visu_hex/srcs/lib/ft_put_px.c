#include <visu_hex.h>

void		ft_put_px(t_b *b, int x, int y, t_ui color)
{
	int		i;
	t_rgb	rgb;

	rgb = *(t_rgb*)&color;
	i = x * 4 + y * WIDTH * 4;
	if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT || i < 0 ||
			i > HEIGHT * WIDTH * 4)
		return ;
	b->img.str[i] = (char)rgb.b;
	b->img.str[i + 1] = (char)rgb.g;
	b->img.str[i + 2] = (char)rgb.r;
}
