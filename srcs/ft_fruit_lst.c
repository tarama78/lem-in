#include <lem_in.h>

void		ft_fruit_lst(t_lst *lst, void (*f)(t_lst *lst))
{
	t_lst	*free_lst;

	while (lst)
	{
		free_lst = lst;
		lst = lst->next;
		f(free_lst);
		free(free_lst);
	}
}
